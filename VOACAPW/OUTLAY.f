c###outlay.for
      SUBROUTINE OUTLAY
C--------------------------------
C.....THIS ROUTINE TABULATES FOT-MUF (FULL IONOSPHERE)
      COMMON / FILES / LUI, LUO, LU2, LU5, LU6, LU15, LU16, LU20, LU25,
     A LU26, LU35
      COMMON / MUFS / EMUF(24), F1MUF(24), F2MUF(24), ESMUF(24),
     A ALLMUF(24), FOT(24), XLUF(24), HPF(24), ANGMUF(24), MODMUF,
     B SIGL(4), SIGU(4), DELMUF(4), HPMUF(4), HTMUF(4), FVMUF(4),
     C AFMUF(4), NHOPMF(4), YFOT(4), YHPF(4), YMUF(4)
      COMMON / OUTPRT / LINBOT(26), LINBD(14), LINTOP(15), LINTP(14),
     A GRPTYP, JOUT, LINBYP, LINES, LINMAX, LINTYP, LPAGES, NLINE
      COMMON / TIME / IT, GMT, UTIME(24), GMTR, XLMT(24), ITIM, JTX
      IF(LINES - LINMAX) 110, 100, 100
  100 CALL OUTTOP
      WRITE(LUO,1502)
      WRITE(LUO,1504)
      LINES = LINES + 4
  110 WRITE(LUO,1506) UTIME(IT),XLMT(IT), (YFOT(I),YMUF(I),YHPF(I),
     A DELMUF(I), HPMUF(I), HTMUF(I), FVMUF(I), I=1,2)
      WRITE(LUO,1508) (YFOT(I), YMUF(I), YHPF(I), DELMUF(I), HPMUF(I),
     A HTMUF(I), FVMUF(I), I=3,4)
      LINES = LINES + 2
       RETURN
C.....FORMAT SPECIFICATIONS
 1502 FORMAT(/,26X,'ELAYER/F2LAYER',28X,'F1LAYER(E)/ESLAYER',/)
 1504 FORMAT(1X,' GMT   LMT',2('   FOT   MUF   HPF ANGLE VIRTL  TRUE ',
     A 'FVERT',2X),/)
 1506 FORMAT(' ',F4.1,F6.1,2(4F6.1,2F6.0,F6.1,2X))
 1508 FORMAT(11X,2(4F6.1,2F6.0,F6.1,2X))
      END
